            'Units','normalized',...
            'Position',[0.83 0.7 .04 .025],...
            'BackgroundColor','w',...
            'ForegroundColor','black',...
            'String',N,...
            'fontsize', 10,...
            'Tag','tens_N',...
            'Callback','N=str2num(get(gco,''string''));close;test(A,f,N,VDC)');
        
        
        %SEMNAL SINUSOIDAL
        Vi=A*sin(w*t);
        subplot(3,3,1);
plot(t,Vi,'-r');
grid on;
title('Semnalul Sinusoidal','FontSize',13);
xlabel('timp[ms]');
ylabel('Amplitudine [V]');


z=uicontrol('Style','Checkbox',...
 'Units','normalized',...
 'String','COSINUS',...
 'BackgroundColor','#d3d3d3',...
 'Position', [0.6 .7 .1 .1],...
 'Callback','cos=1;close;test(A,f,N,VDC)')



switch cos
    case 1
  subplot(3,3,1);
plot(t,Vi,'-r');
grid on;
title('Semnalul cos','FontSize',13);
xlabel('timp[ms]');
ylabel('Amplitudine [V]');
    case 2
 subplot(3,3,1);
plot(t,Vi,'-r');
grid on;
title('Semnalul Sinusoidal','FontSize',13);
xlabel('timp[ms]');
ylabel('Amplitudine [V]');
end
        
if cos==1
   % Vi=A*sin(w*t)+pi/2;
   set(z,'value',1)
    set(z,'value',0)
  
else
     %  Vi=A*sin(w*t);
      set(z,'value',0)
       set(z,'value',1)
     
 
end

 BVDC=uicontrol('Style','text',...
              'Units','normalized',...
              'Position',[0.8 0.6 .03 .02],...
              'backgroundcolor','#d3d3d3',...
              'fontsize', 10,...
              'foregroundcolor','black',...
              'String','VDC=');

BVDCe=uicontrol('Style','edit',...
            'Units','normalized',...
            'Position',[0.83 0.6 .04 .025],...
            'BackgroundColor','w',...
            'ForegroundColor','black',...
            'String',VDC,...
            'fontsize', 10,...
            'Tag','tens_VDC',...
            'Callback','VDC=str2num(get(gco,''string''));close;test(A,f,N,VDC)');
        
        %semnal DC
        

dc=VDC+A*sin(2*pi*f*t);
subplot(3,3,7);
plot(t,dc,'-r');
grid on;
title('Semnalul DC','FontSize',13);
xlabel('timp[ms]');
ylabel('Amplitudine [V]');







