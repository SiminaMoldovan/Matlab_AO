            'Units','normalized',...
            'Position',[0.83 0.8 .04 .025],...
            'BackgroundColor','w',...
            'ForegroundColor','black',...
            'String',A,...
            'fontsize', 10,...
            'Tag','tens_A',...
            'Callback','A=str2num(get(gco,''string''));close;test(A,f,N,VDC)');
        
        
        Bf=uicontrol('Style','text',...
              'Units','normalized',...
              'Position',[0.8 0.75 .03 .02],...
              'backgroundcolor','#d3d3d3',...
              'fontsize', 10,...
              'foregroundcolor','black',...
              'String','f=');

Bfe=uicontrol('Style','edit',...
            'Units','normalized',...
            'Position',[0.83 0.75 .04 .025],...
            'BackgroundColor','w',...
            'ForegroundColor','black',...
            'String',f,...
            'fontsize', 10,...
            'Tag','tens_f',...
            'Callback','f=str2num(get(gco,''string''));close;test(A,f,N,VDC)');
        
          
        BN=uicontrol('Style','text',...
              'Units','normalized',...
              'Position',[0.8 0.7 .03 .02],...
              'backgroundcolor','#d3d3d3',...
              'fontsize', 10,...
              'foregroundcolor','black',...
              'String','N=');

